  @doc """
  Converts a pixel art text grid to an SVG string.
  '#' = black pixel, '.' = transparent
  """
  def pixels(text) do
    rows =
      text
      |> String.trim()
      |> String.split("\n")
      |> Enum.map(&String.trim/1)

    rects =
      rows
      |> Enum.with_index()
      |> Enum.flat_map(fn {row, y} ->
        row
        |> String.graphemes()
        |> Enum.with_index()
        |> Enum.filter(fn {char, _x} -> char == "#" end)
        |> Enum.map(fn {_char, x} ->
          ~s(<rect x="#{x}" y="#{y}" width="1" height="1"/>)
        end)
      end)
      |> Enum.join("\n")

    """
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 32 20" shape-rendering="crispEdges">
    <g fill="#000">
    #{rects}
    </g>
    </svg>
    """
  end
